set1 = {"andrea", "mario", "simone", "davide"}
set2 = {"mario", "roberto", "simone"}

print("Set sottrazione")
print(set1 - set2)
print("-" * 30)

print("Set unione")
print(set1 | set2)
print("-" * 30)


print("Set intersezione")
print(set1 & set2)
print("-" * 30)


print("Set esclusione")
print(set1 ^ set2)
print("-" * 30)



